import kotlinx.io.files.Path
import org.jraf.klibnanolog.logd
import org.jraf.klibperipage.Peripage

suspend fun main(av: Array<String>) {
  Peripage().connect {
    logd("Connected to PeriPage printer: $printer")
    printer.printImage(Path(av[0]))
    printer.lineFeed(80u)
  }
}
